    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Db db = Db.getObj();
        if(val()){
            if(jButton1.getText().equals(SAVE_BUTTON)){
                String d[] = catList.get(jComboBox1.getSelectedIndex()-1);
                int catId = Integer.parseInt(d[0]);

                int i = db.insertProducts(jTextField1.getText(), 
                        catId, Double.parseDouble(jTextField2.getText()),Double.parseDouble(jTextField3.getText()));
                
                Utils.showMessage(this, i);
                if(ab != null){
                    ab.updateProducts();
                    this.dispose();
                }
                clearUI();
            }
            else{
                String d[] = catList.get(jComboBox1.getSelectedIndex()-1);
                int catId = Integer.parseInt(d[0]);
                
                int i = db.updateProducts(jTextField1.getText(), catId, 
                        Double.parseDouble(jTextField2.getText()),id,Double.parseDouble(jTextField3.getText()));
                
                if(v != null)
                    v.updateTable(); 
                    
                Utils.showMessage(this, i);
                clearUI();
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

