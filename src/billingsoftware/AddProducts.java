    private boolean val() {
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Name can't be empty");
            return false;
        }
        
        else if(jComboBox1.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(this, "Please select the category");
            return false;
        }
        
        else if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Price can't be empty");
            return false;
        }
        
        try{
            Double.parseDouble(jTextField2.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Price should be number");
            return false;
        }
        
        try{
            Double.parseDouble(jTextField3.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Purchase Price should be number");
            return false;
        }
        
        return true;
    }

    public void init() {
        jComboBox1.removeAllItems();
        //todo get all the categories from the database
        Db db = Db.getObj();
        catList = db.getAllCategory();
        int s = catList.size();
        jComboBox1.addItem("Select");
        for(int i=0;i<s;i++){
            String [] d = catList.get(i);
            jComboBox1.addItem(d[1]);
        }
    }

    private void init1() {
        //category
        Db db = Db.getObj();
        catList = db.getAllCategory();
        int s = catList.size();
        jComboBox1.addItem("Select");
        for(int i=0;i<s;i++){
            String [] d = catList.get(i);
            jComboBox1.addItem(d[1]);
        }
        
        //getting values
        String []da = db.getProById(id);
        jTextField1.setText(da[0]);
        jTextField2.setText(da[2]);
        jTextField3.setText(da[4]);
        jLabel7.setText(da[3]);
        
        String catId = da[1];
        int ind = 0;
        for(int i=0;i<s;i++){
            ind = i;
            String [] d = catList.get(i);
            if(catId.equals(d[0]))
                break;
        }
        
        jComboBox1.setSelectedIndex(ind+1);
        
        
    }

    private void clearUI() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jComboBox1.setSelectedIndex(0);
    }
