    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        if(!billList.isEmpty()){
            billSearchList.clear();
            String searchTxt = jTextField1.getText().toLowerCase();
            
            
            int s = billList.size();
            for(int i= 0;i<s;i++){
                String data[] = billList.get(i);
                
                String name = data[2].toLowerCase();
                String mobile = data[3].toLowerCase();
                                
                if(name.contains(searchTxt) || mobile.contains(searchTxt)){
                    billSearchList.add(data);
                }
            }
            setTable(billSearchList);
        }
    }//GEN-LAST:event_jTextField1KeyReleased

