    private void setTable() {
        dm = (DefaultTableModel) jTable1.getModel();
        
        dm.addColumn("InvNo");
        dm.addColumn("Date");
        dm.addColumn("Customer");
        dm.addColumn("Mobile");
    }
    
    private void setTable(List<String[]> billList) {
        dm.setRowCount(0);
        
        int s = billList.size();
        for(int i=0;i<s;i++){
            String d[] = billList.get(i);
            dm.addRow(d);
            dm.fireTableDataChanged();
        }
    }

    private void initUI() {
        setTable();
    }
    
