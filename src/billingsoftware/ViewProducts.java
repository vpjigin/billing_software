    public void init(){
        //category
        Db db = Db.getObj();
        catList = db.getAllCategory();
        int s = catList.size();
        jComboBox1.addItem("All");
        for(int i=0;i<s;i++){
            String [] d = catList.get(i);
            jComboBox1.addItem(d[1]);
        }
    }
    
    public void updateTable(){
        jComboBox1.setSelectedIndex(0);
        dm.setRowCount(0);
    }

    private void setTable() {
        dm = (DefaultTableModel) jTable2.getModel();
        
        dm.addColumn("slNo");
        dm.addColumn("Name");
        dm.addColumn("Category");
        dm.addColumn("Price");
        dm.addColumn("id");
        jTable2.removeColumn(jTable2.getColumnModel().getColumn(4));//hiding the id column
    }

    private void setTableData(List<String[]>list) {
        dm.setRowCount(0);
        
        int s = list.size();
        for(int i=0;i<s;i++){
            String d[] = list.get(i);
            String da[] = new String[]{i+1+"",d[0],d[1],d[2],d[3]};
            dm.addRow(da);
            dm.fireTableDataChanged();
        }
    }

    private void go() {
        Db db = Db.getObj();
        dm.setRowCount(0);
        if(jComboBox1.getSelectedIndex() == 0){
            //all products
            productList = db.getAllProducts();
            setTableData(productList);
        }
        else{
            //only perticular product
            int index = jComboBox1.getSelectedIndex();
            String da[] = catList.get(index-1);
            
            productList = db.getAllProductsByCatId(da[0]);
            setTableData(productList);
        }
    }
