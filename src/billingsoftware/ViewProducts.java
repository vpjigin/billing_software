    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        searchList.clear();
        
        String searchTxt = jTextField1.getText().toLowerCase();
        
        if(!productList.isEmpty()){
            int s = productList.size();
            for(int i= 0;i<s;i++){
                String []data = productList.get(i);
                String productName = data[0].toLowerCase();
                String categoryName = data[1].toLowerCase();
                
                if(productName.contains(searchTxt) || categoryName.contains(searchTxt)){
                    searchList.add(data);
                }
            }
            setTableData(searchList);
        }
    }//GEN-LAST:event_jTextField1KeyReleased

